    private void tb_khachhnagMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_khachhnagMouseClicked
        // TODO add your handling code here:
        int row=tb_khachhnag.getSelectedRow();
        ArrayList<KhachHang> list=ser.lisKH;
        txt_ten.setText(tb_khachhnag.getValueAt(row, 0).toString());
        txt_soluong.setText(tb_khachhnag.getValueAt(row, 2).toString());
        cb_loaive.setSelectedItem(tb_khachhnag.getValueAt(row, 1));
        KhachHang kh=list.get(row);
        if (kh.getGioitinh().equalsIgnoreCase("Nam")) {
            rd_nam.setSelected(true);
        }else{
        rd_nu.setSelected(true);
        }
    }//GEN-LAST:event_tb_khachhnagMouseClicked

