void addRow(){
defaultTableModel=(DefaultTableModel) tb_khachhnag.getModel();
defaultTableModel.setRowCount(0);
ArrayList<KhachHang> lissst=ser.lisKH;
    for (KhachHang khachHang : lissst) {
      defaultTableModel.addRow(new Object[]{
      khachHang.getTen(),khachHang.getLoaive(),khachHang.getSoluong(),khachHang.getGioitinh()
      });
    }
}
    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        if (txt_ten.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "NHập lại tên");
        }else if(txt_soluong.getText().equals("")){
        JOptionPane.showMessageDialog(this, "nhập số lượng");
        }else{
        KhachHang kh=new KhachHang();
        kh.setTen(txt_ten.getText());
        kh.setLoaive(cb_loaive.getSelectedItem().toString());
            int so = 0;
            try {
                so = Integer.parseInt(txt_soluong.getText());
            } catch (NumberFormatException numberFormatException) {
                JOptionPane.showMessageDialog(this, "Phải nhập số");
            }
            if (so<=0) {
                JOptionPane.showMessageDialog( this, "mời nhập lại số");
            }else{
            kh.setSoluong(so);
            }
            if (rd_nam.isSelected()) {
                kh.setGioitinh("Nam");
              
                
            }else{
            kh.setGioitinh("Nữ");
            }
            Boolean check=ser.them(kh);
            if (check==true) {
                JOptionPane.showMessageDialog(this, "them thành công");
           addRow();
            }else{
            JOptionPane.showMessageDialog(this, "them that bai");
            }
        }
    }//GEN-LAST:event_btn_themActionPerformed

