    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Sach s = this.getFormData();
        if (s == null) {
            return;
        }
        for (Sach sa : this.ser.getList()) {
            if (sa.getMa().equals(txtMa.getText().trim())) {
                JOptionPane.showMessageDialog(this, "trug ma khong them dc");
                return;
            }
        }
        this.ser.insert(s);
        this.loadtable();
        this.clearForm();
    }//GEN-LAST:event_btnThemActionPerformed

