    private void tbSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSachMouseClicked
        // TODO add your handling code here:
        int row = tbSach.getSelectedRow();
        txtDon.setText(tbSach.getValueAt(row, 4).toString());
        txtMa.setText(tbSach.getValueAt(row, 0).toString());
        txtTen.setText(tbSach.getValueAt(row, 1).toString());
        txtNgay.setText(tbSach.getValueAt(row, 2).toString());
        txtSo.setText(tbSach.getValueAt(row, 3).toString());
        if (tbSach.getValueAt(row, 5).toString().equals("Còn")) {
            rdCon.setSelected(true);
        } else {
            rdHet.setSelected(true);
        }
    }//GEN-LAST:event_tbSachMouseClicked

